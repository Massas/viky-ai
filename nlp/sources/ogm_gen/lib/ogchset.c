PUBLIC(int) OgCharsetToCode(char *charset)
{
int i;
for (i=0; OgCharset[i].MIBenum!=0; i++) {
  if (!Ogstricmp(charset,OgCharset[i].name)) return(OgCharset[i].MIBenum);
  }
return(0);
}





PUBLIC(char *) OgCodeToCharset(int code)
{
int i;
for (i=0; OgCharset[i].MIBenum!=0; i++) {
  if (code==OgCharset[i].MIBenum) return(OgCharset[i].name);
  }
return("nil");
}



