PUBLIC(char *) PcStrlwr(char *s)
{
int i;
for (i=0; s[i]!=0; i++)
  s[i]=DPcTolower((unsigned char)s[i]);
return(s); 
}


PUBLIC(char *) PcStrupr(char *s)
{
int i;
for (i=0; s[i]!=0; i++)
  s[i]=DPcToupper((unsigned char)s[i]);
return(s); 
}





